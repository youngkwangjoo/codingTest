def solution(my_string):
    # 알파벳 딕셔너리 정의 (1부터 52까지의 key에 알파벳 대소문자를 매핑)
    alpa = {
        1: 'A',  2: 'B',  3: 'C',  4: 'D',  5: 'E',  6: 'F',  7: 'G',  8: 'H',  9: 'I', 10: 'J',
        11: 'K', 12: 'L', 13: 'M', 14: 'N', 15: 'O', 16: 'P', 17: 'Q', 18: 'R', 19: 'S', 20: 'T',
        21: 'U', 22: 'V', 23: 'W', 24: 'X', 25: 'Y', 26: 'Z', 27: 'a', 28: 'b', 29: 'c', 30: 'd',
        31: 'e', 32: 'f', 33: 'g', 34: 'h', 35: 'i', 36: 'j', 37: 'k', 38: 'l', 39: 'm', 40: 'n',
        41: 'o', 42: 'p', 43: 'q', 44: 'r', 45: 's', 46: 't', 47: 'u', 48: 'v', 49: 'w', 50: 'x',
        51: 'y', 52: 'z'
    }

    # 52개의 빈도 리스트 초기화
    answer = [0] * 52
    
    # my_string에 있는 각 문자를 확인하여 해당하는 인덱스의 빈도를 증가
    for char in my_string:
        for key, value in alpa.items():
            if char == value:  # 문자가 딕셔너리 값과 일치하면
                answer[key - 1] += 1  # 해당 키에 대응하는 인덱스의 값을 1 증가시킴 (key - 1은 0부터 시작하는 리스트 인덱스)
                break  # 매칭된 문자를 찾았으므로 반복문을 빠져나감
    
    return answer
